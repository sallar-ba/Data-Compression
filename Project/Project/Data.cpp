#include "Data.h"
//-----------------------------------------------
Data::Data()
{
	this->Freq = 0;
	this->Symbol = ' ';
}
//------------------------------------------------
//-----------------------------------------------
Data::Data(char Symbol)
{
	this->Freq = 1;
	this->Symbol = Symbol;
}
//------------------------------------------------
//-----------------------------------------------
void Data::insert(char Symbol)
{
	this->Freq = 1;
	this->Symbol = Symbol;
}
//------------------------------------------------
//-----------------------------------------------
void Data::insert(char Symbol, int Freq)
{
	this->Freq = Freq;
	this->Symbol = Symbol;
}
